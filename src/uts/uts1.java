    private void ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubahActionPerformed
        DefaultTableModel dataModel = (DefaultTableModel) tabelData.getModel();

    int selectedRow = tabelData.getSelectedRow();
    if (selectedRow != -1) { 
    List<Object> list = new ArrayList<>();

    
    list.add(idBarang.getText());
    list.add(nmBarang.getText());
    list.add(satuan.getSelectedItem().toString());
    list.add(jumlah.getText());
    list.add(hrgSatuan.getText());
    list.add(tgl.getDate().toString());

    double hs = Double.parseDouble(hrgSatuan.getText());
    double jm = Double.parseDouble(jumlah.getText());
    list.add(hs * jm);

    
    for (int i = 0; i < list.size(); i++) {
        dataModel.setValueAt(list.get(i), selectedRow, i);
    }
} else {
    JOptionPane.showMessageDialog(null, "Pilih Kolom yang mau Di Edit", "Tidak Ada Kolom yang di pilih", JOptionPane.WARNING_MESSAGE);
}

    }//GEN-LAST:event_ubahActionPerformed

