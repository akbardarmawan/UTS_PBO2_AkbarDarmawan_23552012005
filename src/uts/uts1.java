    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
       double hs, jm;
       DefaultTableModel dataModel = (DefaultTableModel) tabelData.getModel();
       List list = new ArrayList<>();
       tabelData.setAutoCreateColumnsFromModel(true);
       
       list.add(idBarang.getText());
       list.add(nmBarang.getText());
       list.add(satuan.getSelectedItem().toString());
       list.add(jumlah.getText());
       list.add(hrgSatuan.getText());
       list.add(tgl.getDate().toString());

       hs = Double.parseDouble(String.valueOf(hrgSatuan.getText()));
       jm = Double.parseDouble(String.valueOf(jumlah.getText()));
       list.add(hs*jm);
       
       dataModel.addRow(list.toArray());
       
       idBarang.setText("");
        nmBarang.setText("");
        satuan.setSelectedIndex(0);
        jumlah.setText("");
        hrgSatuan.setText("");
        tgl.setDate(null);
    }//GEN-LAST:event_tambahActionPerformed

