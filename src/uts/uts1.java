    private void cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariActionPerformed
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String searchId = cariData.getText();
        DefaultTableModel model = (DefaultTableModel) tabelData.getModel();
        int rowCount = model.getRowCount();
        boolean found = false;
        
        for (int i = 0; i < rowCount; i++) {
            String id = model.getValueAt(i, 0).toString(); 
            if (id.equals(searchId)) {
                
                idBarang.setText(model.getValueAt(i, 0).toString());
                nmBarang.setText(model.getValueAt(i, 1).toString());
                satuan.setSelectedItem(model.getValueAt(i, 2).toString());
                jumlah.setText(model.getValueAt(i, 3).toString());
                hrgSatuan.setText(model.getValueAt(i, 4).toString());
                Date date = null;
                try {
                    date = dateFormat.parse(model.getValueAt(i, 5).toString()); 
                    filler.setText(dateFormat.format(date));
                } catch (ParseException ex) {
                    Logger.getLogger(uts1.class.getName()).log(Level.SEVERE, null, ex);
                }
                found = true;
                break;
            }
        }
        
        if (!found) {
            JOptionPane.showMessageDialog(null, "Barang Tidak Ditemui", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_cariActionPerformed

